#include "../header/fdf.h"

void	zoom(int *p1, int *p2)
{
	p1[0] *= 20;
	p1[1] *= 20;
	p2[0] *= 20;
	p2[1] *= 20;
}

void	increase_height(int *pa, int x1, int y1, t_data *dt)
{
	int	p2[2];
	int	p1[2];

	p1[0] = pa[0];
	p1[1] = pa[1];
	p2[0] = x1;
	p2[1] = y1;
	DDA(p1, p2, dt);
}
